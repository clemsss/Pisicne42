char	*ft_strncat(char *dest, char *src, int nb)
{
	int i;
	int n;

	n = 0;
	i = 0;
	while (dest[n])
		n++;
	while (i < nb)
	{
		dest[n] = src[i];
		i++;
		n++;
	}
	dest[n] = '\0';
	return (dest);
}
