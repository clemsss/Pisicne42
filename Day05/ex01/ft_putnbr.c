void	ft_putchar(char c);

void	ft_putnbr(int nb)
{
	long int	i;

	i = nb;
	if (i < 0)
	{
		ft_putchar('-');
		i = i * -1;
	}
	if (i >= 10)
	{
		ft_putnbr(i / 10);
	}
	i = i % 10 + '0';
	ft_putchar(i);
}
